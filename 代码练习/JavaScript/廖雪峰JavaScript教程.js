// 语句块是一组语句的集合。例：先做一个判断，如果判断成立，将执行{...}中的所有语句
if (2 > 1) {
    x = 1;  // 注：花括号{...}内的语句具有缩进，通常是4个空格。缩进不是JavaScript语法要求必须的，但有助于我们理解代码的层次，所以要遵守缩进规则。n
    y = 2;
    z = 3;
}




